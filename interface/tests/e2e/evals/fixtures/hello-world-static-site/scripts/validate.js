const fs = require("node:fs");
const path = require("node:path");

const htmlPath = path.join(process.cwd(), "index.html");

if (!fs.existsSync(htmlPath)) {
  console.error("Expected index.html to exist.");
  process.exit(1);
}

const html = fs.readFileSync(htmlPath, "utf8");

const checks = [
  { label: "title", passed: html.includes("<title>Hello Aura</title>") },
  { label: "heading", passed: html.includes("Hello from Aura") },
  { label: "tagline", passed: html.includes('data-testid="tagline"') && html.includes("This page was generated by Aura.") },
];

const failures = checks.filter((check) => !check.passed);

if (failures.length > 0) {
  console.error(`Validation failed for: ${failures.map((check) => check.label).join(", ")}`);
  process.exit(1);
}

console.log("Hello world benchmark fixture validated successfully.");
