#!/usr/bin/env bash

set -euo pipefail

# When launched as an absolute Git-for-Windows bash.exe from PowerShell, PATH may
# not include the Unix helper directory that contains dirname and friends.
PATH="/usr/bin:/bin:$PATH"
export PATH

script_dir="$(cd "$(dirname "$0")" && pwd)"
# shellcheck disable=SC1091
source "$script_dir/common.sh"

stack_load_env
stack_validate_modes
stack_mkdir_runtime
stack_warn_if_missing_auth_secret

write_export() {
  local file="$1"
  local key="$2"
  local value="$3"
  printf 'export %s=%q\n' "$key" "$value" >>"$file"
}

write_optional_export() {
  local file="$1"
  local key="$2"
  local value="$3"
  if [[ -n "$value" ]]; then
    write_export "$file" "$key" "$value"
  else
    printf 'unset %s\n' "$key" >>"$file"
  fi
}

write_header() {
  local file="$1"
  cat >"$file" <<'EOF'
#!/usr/bin/env bash
# Generated by evals/local-stack/bin/render-envs.sh
# Safe to source with: set -a; source <file>; set +a
EOF
}

network_url="$(stack_resolved_url network)"
storage_url="$(stack_resolved_url storage)"
orbit_url="$(stack_resolved_url orbit)"
harness_url="$(stack_resolved_url harness)"

network_env="$AURA_STACK_RUNTIME_DIR/aura-network.env"
storage_env="$AURA_STACK_RUNTIME_DIR/aura-storage.env"
orbit_env="$AURA_STACK_RUNTIME_DIR/orbit.env"
aura_os_env="$AURA_STACK_RUNTIME_DIR/aura-os.env"
evals_env="$AURA_STACK_RUNTIME_DIR/evals.env"

write_header "$network_env"
write_export "$network_env" "DATABASE_URL" "postgresql://postgres:postgres@127.0.0.1:${AURA_STACK_NETWORK_DB_PORT}/aura_network"
write_export "$network_env" "PORT" "$AURA_STACK_NETWORK_PORT"
write_export "$network_env" "AUTH0_DOMAIN" "$AURA_STACK_AUTH0_DOMAIN"
write_export "$network_env" "AUTH0_AUDIENCE" "$AURA_STACK_NETWORK_AUDIENCE"
write_export "$network_env" "AUTH_COOKIE_SECRET" "$AURA_STACK_AUTH_COOKIE_SECRET"
write_export "$network_env" "INTERNAL_SERVICE_TOKEN" "$AURA_STACK_INTERNAL_SERVICE_TOKEN"
write_export "$network_env" "CORS_ORIGINS" "$AURA_STACK_CORS_ORIGINS"
write_export "$network_env" "LOG_LEVEL" "$AURA_STACK_NETWORK_LOG_LEVEL"
write_export "$network_env" "AURA_NETWORK_DEV_TRUST_TOKENS" "1"
write_export "$network_env" "ZOS_API_URL" "$AURA_STACK_ZOS_API_URL"
write_optional_export "$network_env" "ZOS_API_INTERNAL_TOKEN" "$AURA_STACK_ZOS_API_INTERNAL_TOKEN"
write_optional_export "$network_env" "AURA_STORAGE_URL" "$storage_url"

write_header "$storage_env"
write_export "$storage_env" "DATABASE_URL" "postgresql://postgres:postgres@127.0.0.1:${AURA_STACK_STORAGE_DB_PORT}/aura_storage"
write_export "$storage_env" "PORT" "$AURA_STACK_STORAGE_PORT"
write_export "$storage_env" "AUTH0_DOMAIN" "$AURA_STACK_AUTH0_DOMAIN"
write_export "$storage_env" "AUTH0_AUDIENCE" "$AURA_STACK_STORAGE_AUDIENCE"
write_export "$storage_env" "AUTH_COOKIE_SECRET" "$AURA_STACK_AUTH_COOKIE_SECRET"
write_export "$storage_env" "INTERNAL_SERVICE_TOKEN" "$AURA_STACK_INTERNAL_SERVICE_TOKEN"
write_export "$storage_env" "CORS_ORIGINS" "$AURA_STACK_CORS_ORIGINS"
write_export "$storage_env" "RUST_LOG" "$AURA_STACK_STORAGE_LOG_LEVEL"
write_export "$storage_env" "AURA_STORAGE_DEV_TRUST_TOKENS" "1"
write_optional_export "$storage_env" "AURA_NETWORK_URL" "$network_url"
write_optional_export "$storage_env" "AURA_NETWORK_TOKEN" "$AURA_STACK_INTERNAL_SERVICE_TOKEN"

write_header "$orbit_env"
write_export "$orbit_env" "DATABASE_URL" "postgresql://postgres:postgres@127.0.0.1:${AURA_STACK_ORBIT_DB_PORT}/orbit"
write_export "$orbit_env" "SERVER_HOST" "0.0.0.0"
write_export "$orbit_env" "SERVER_PORT" "$AURA_STACK_ORBIT_PORT"
write_export "$orbit_env" "GIT_STORAGE_ROOT" "$AURA_STACK_ORBIT_GIT_STORAGE_ROOT"
write_export "$orbit_env" "LOG_LEVEL" "$AURA_STACK_ORBIT_LOG_LEVEL"
write_export "$orbit_env" "CORS_ORIGINS" "$AURA_STACK_CORS_ORIGINS"
write_export "$orbit_env" "PUBLIC_BASE_URL" "$(stack_local_url orbit)"
write_export "$orbit_env" "AUTH0_DOMAIN" "$AURA_STACK_AUTH0_DOMAIN"
write_export "$orbit_env" "AUTH0_AUDIENCE" "$AURA_STACK_ORBIT_AUDIENCE"
write_export "$orbit_env" "AUTH_COOKIE_SECRET" "$AURA_STACK_AUTH_COOKIE_SECRET"
write_export "$orbit_env" "INTERNAL_SERVICE_TOKEN" "$AURA_STACK_INTERNAL_SERVICE_TOKEN"
write_optional_export "$orbit_env" "AURA_NETWORK_URL" "$network_url"

write_header "$aura_os_env"
write_export "$aura_os_env" "AURA_SERVER_PORT" "$AURA_STACK_AURA_OS_PORT"
write_export "$aura_os_env" "AURA_DATA_DIR" "$AURA_STACK_AURA_OS_DATA_DIR"
write_export "$aura_os_env" "AURA_ALLOW_AUTH_TOKEN_IMPORT" "1"
write_optional_export "$aura_os_env" "AURA_NETWORK_URL" "$network_url"
write_optional_export "$aura_os_env" "AURA_STORAGE_URL" "$storage_url"
write_optional_export "$aura_os_env" "ORBIT_BASE_URL" "$orbit_url"
write_optional_export "$aura_os_env" "SWARM_BASE_URL" "$harness_url"
if stack_is_local harness; then
  write_optional_export "$aura_os_env" "LOCAL_HARNESS_URL" "$harness_url"
  write_optional_export "$aura_os_env" "AURA_HARNESS_DIR" "$AURA_STACK_HARNESS_DIR"
  write_export "$aura_os_env" "AURA_DISABLE_LOCAL_HARNESS_AUTOSPAWN" "0"
else
  printf 'unset LOCAL_HARNESS_URL\n' >>"$aura_os_env"
  printf 'unset AURA_HARNESS_DIR\n' >>"$aura_os_env"
  write_export "$aura_os_env" "AURA_DISABLE_LOCAL_HARNESS_AUTOSPAWN" "1"
fi
write_optional_export "$aura_os_env" "AURA_ROUTER_URL" "$AURA_STACK_ROUTER_URL"
write_optional_export "$aura_os_env" "Z_BILLING_URL" "$AURA_STACK_BILLING_URL"

write_header "$evals_env"
write_export "$evals_env" "AURA_EVAL_LIVE" "1"
write_export "$evals_env" "AURA_EVAL_BASE_URL" "$AURA_STACK_EVAL_BASE_URL"
write_export "$evals_env" "AURA_EVAL_API_BASE_URL" "$(stack_local_url aura_os)"
write_export "$evals_env" "AURA_EVAL_BUNDLE_ID" "$AURA_STACK_EVAL_BUNDLE_ID"
write_optional_export "$evals_env" "AURA_EVAL_STORAGE_URL" "$storage_url"
if stack_is_local harness; then
  write_export "$evals_env" "AURA_EVAL_AGENT_MACHINE_TYPE" "local"
else
  write_export "$evals_env" "AURA_EVAL_AGENT_MACHINE_TYPE" "remote"
fi

chmod +x "$network_env" "$storage_env" "$orbit_env" "$aura_os_env" "$evals_env"

echo "Rendered local stack env files into $AURA_STACK_RUNTIME_DIR"
