#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize, Default)]
#[serde(rename_all = "snake_case")]
pub enum HarnessMode {
    #[default]
    Local,
    Swarm,
}

impl HarnessMode {
    pub fn from_machine_type(mt: &str) -> Self {
        match mt {
            "local" => Self::Local,
            _ => Self::Swarm,
        }
    }
}

